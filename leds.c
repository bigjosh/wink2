#define PANEL_SIZE_X 14
#define PANEL_SIZE_Y 90

#define PANEL_GAP_X 8
#define PANEL_GAP_Y 0			// Someday make a diaginal one?

#define PANEL_SPACING_X (PANEL_SIZE_X+PANEL_GAP_X)
#define PANEL_SPACING_Y 0						// This instal all panels are in a row

#define PANEL_COUNT 3


#define EXTENT_X ((PANEL_SIZE_X*3) + (PANEL_GAP_X*2))		// Only 2 gaps between 3 panels
