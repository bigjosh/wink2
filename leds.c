// Data is send as r,g,b for each pixel. All strings concatinated together in one packet.

#define PANEL_SIZE  ((PANEL_SIZE_X*2) + (PANEL_SIZE_Y*2) - 4 ) // LEDs along all 4 sides of each panel, minus 4 becuase the corners are counted twice

#define BUFFER_SIZE (PANEL_SIZE*PANEL_COUNT)

struct pixel {
	unsigned char r,g,b;
};

struct pixel pixelbuffer[BUFFER_SIZE];     // Buffer of RGB values, ready to be put into an OPC packet

// Create a table of X,Y values into to the RGB packet buffers
// This is non trivial because we need to account for the fact that the leds go clockwise around the panels, and the gaps between panels

struct coord {
	int x;
	int y;
};

struct coord coords[ BUFFER_SIZE ];

// coords[0].x is the x location of the point that is in opcbuffer[0]

// 0,0 is upper left pixel as viewed from front
// Left panel is channel #1, middle #2, right #3.
// All are wired CCW as viewed from the front
// 1st pixel of each panel is just to the left of center

int panelOrder[PANEL_COUNT] = { 1,2,0 };		// Mapp the physical order of the panels to the order they are connected to pins (and thus ledscape channels)

void initcoords() {

	int i=0;			// i index into buffer

	for(int p=0;p<PANEL_COUNT;p++) {

		int panelPhysical = panelOrder[p];

		int panel_offset_x = panelPhysical * (PANEL_SPACING_X);		// step to the right
		int panel_offset_y = panelPhysical * (PANEL_SPACING_Y);

		for(int x=6;x>0;x--) {			// go to top left corner
			coords[i].x=x+panel_offset_x;
			coords[i].y=0+panel_offset_y;
			i++;
		}

		for(int y=0;y<(PANEL_SIZE_Y-1);y++) {		// down left side
			coords[i].x=0+panel_offset_x;
			coords[i].y=y+panel_offset_y;
			i++;
		}

		for(int x=0;x<(PANEL_SIZE_X-1);x++) {			// bottom
			coords[i].x=x+panel_offset_x;
			coords[i].y=(PANEL_SIZE_Y-1)+panel_offset_y;
			i++;
		}

		for(int y=PANEL_SIZE_Y-1;y>0;y--) {		// right side
			coords[i].x=(PANEL_SIZE_X-1)+panel_offset_x;
			coords[i].y=y+panel_offset_y;
			i++;
		}

		for(int x=(PANEL_SIZE_X-1);x>6;x--) {			// home strech on top to middle
			coords[i].x=x+panel_offset_x;
			coords[i].y=0+panel_offset_y;
			i++;
		}

	}

}


